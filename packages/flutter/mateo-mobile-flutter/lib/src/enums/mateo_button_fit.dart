/// Controls the width sizing behavior of Mateo Mobile buttons.
///
/// [MateoButtonFit.fit] shrink-wraps the button to its content.
/// [MateoButtonFit.expand] fills the available horizontal width.
enum MateoButtonFit {
  /// The button shrink-wraps to its content.
  fit,

  /// The button expands to fill the available horizontal width.
  expand,
}
